<script>
    document.addEventListener('DOMContentLoaded', function() {
        const logo = document.getElementById('floatingLogo');
        const footer = document.getElementById('site-footer');

        function checkFooterOverlap() {
            // FIX: Check if elements exist before accessing them
            if (!logo || !footer) return;
            
            const logoRect = logo.getBoundingClientRect();
            const footerRect = footer.getBoundingClientRect();

            if (logoRect.bottom > footerRect.top) {
                logo.classList.remove('opacity-20');
                logo.classList.add('opacity-0');
            } else {
                logo.classList.remove('opacity-0');
                logo.classList.add('opacity-20');
            }
        }

        // Only attach listeners if elements exist
        if (logo && footer) {
            window.addEventListener('scroll', checkFooterOverlap);
            window.addEventListener('resize', checkFooterOverlap);
            checkFooterOverlap();
        }
    });
</script>